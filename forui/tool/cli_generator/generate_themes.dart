/// Traverses the library and finds all themes.
Future<ThemesConstructors> traverseThemes(AnalysisContextCollection collection) async {
  final typography = await ConstructorMatch.traverse(collection, _typography, _typographyConstructor, {'FTypography'});
  final style = await ConstructorMatch.traverse(collection, _style, _styleConstructor, {'FStyle'});

  // Parse colors.dart to build a map from field name to inline FColors(...) expression.
  final colors = <String, String>{};
  if (await collection.contextFor(_colors).currentSession.getResolvedUnit(_colors)
      case final ResolvedUnitResult result) {
    final visitor = _ColorsVisitor();
    result.unit.accept(visitor);
    colors.addAll(visitor.colors);
  }

  // Parse icons.dart to build a map from FIcons field name to its FIcons.lucide() initializer expression.
  final icons = <String, String>{};
  if (await collection.contextFor(_icons).currentSession.getResolvedUnit(_icons) case final ResolvedUnitResult result) {
    final visitor = _IconsVisitor();
    result.unit.accept(visitor);
    icons.addAll(visitor.icons);
  }

  if (await collection.contextFor(_themes).currentSession.getResolvedUnit(_themes)
      case final ResolvedUnitResult result) {
    final visitor = _ThemesVisitor(colors);
    result.unit.accept(visitor);

    return (typography: typography, icons: icons, style: style, themes: visitor.themes);
  }

  throw Exception('Failed to parse $_colors & $_themes');
}

class _ColorsVisitor extends RecursiveAstVisitor<void> {
  final Map<String, String> colors = {};
  bool _inside = false;

  @override
  void visitClassDeclaration(ClassDeclaration declaration) {
    if (declaration.namePart.typeName.lexeme == 'FColors') {
      _inside = true;
      super.visitClassDeclaration(declaration);
      _inside = false;
    }
  }

  @override
  void visitFieldDeclaration(FieldDeclaration field) {
    if (!_inside || !field.isStatic) {
      return;
    }

    for (final variable in field.fields.variables) {
      if (variable.initializer != null) {
        colors[variable.name.lexeme] = variable.initializer!.toSource().replaceAll('FColors._', 'FColors');
      }
    }
  }
}

class _IconsVisitor extends RecursiveAstVisitor<void> {
  // Icon name -> default initializer expression (qualified, e.g. `FIcons.iconData(FLucideIcons.arrowLeft)`).
  final Map<String, String> icons = {};
  bool _inside = false;

  @override
  void visitClassDeclaration(ClassDeclaration declaration) {
    if (declaration.namePart.typeName.lexeme == 'FIcons') {
      _inside = true;
      super.visitClassDeclaration(declaration);
      _inside = false;
    }
  }

  @override
  void visitConstructorDeclaration(ConstructorDeclaration declaration) {
    if (_inside && declaration.name?.lexeme == 'lucide') {
      for (final initializer in declaration.initializers) {
        if (initializer case RedirectingConstructorInvocation(argumentList: ArgumentList(:final arguments))) {
          for (final NamedExpression(:name, :expression) in arguments.whereType<NamedExpression>()) {
            icons[name.label.name] = 'FIcons.${expression.toSource()}';
          }
        }
      }
    }
  }
}

class _ThemesVisitor extends RecursiveAstVisitor<void> {
  final Map<String, String> _colors;
  final Map<(String, String?), List<ThemeConstructor>> themes = {};

  _ThemesVisitor(this._colors);

  @override
  void visitFieldDeclaration(FieldDeclaration field) {
    if (!field.isStatic) {
      return;
    }

    for (final variable in field.fields.variables) {
      final theme = variable.name.lexeme;

      // Match (light: FPlatformThemeData(desktop: () => FThemeData(...), touch: () => FThemeData(...)), dark: ...)
      if (variable.initializer case final RecordLiteral autoTheme) {
        for (final NamedExpression(:name, :expression) in autoTheme.fields.whereType<NamedExpression>()) {
          final variant = name.label.name; // "light" or "dark"

          // FPlatformThemeData(desktop: () => FThemeData(...), touch: () => FThemeData(...))
          if (expression case final InstanceCreationExpression platformTheme) {
            // Extract colors from the touch variant
            for (final NamedExpression(:name, :expression)
                in platformTheme.argumentList.arguments.whereType<NamedExpression>()) {
              if (name.label.name != 'touch') {
                continue;
              }

              // () => FThemeData(colors: ...) — unwrap the closure
              final themeDataExpr = switch (expression) {
                FunctionExpression(body: ExpressionFunctionBody(:final expression)) => expression,
                _ => expression,
              };

              // FThemeData(colors: ...)
              if (themeDataExpr case final InstanceCreationExpression themeData) {
                var colors = '';
                for (final expression in themeData.argumentList.arguments.whereType<NamedExpression>()) {
                  if (expression.name.label.name == 'colors') {
                    final source = expression.expression.toSource();
                    // Resolve named constants (e.g. FColors.zincLight) to their inline definition.
                    colors = source.startsWith('FColors.')
                        ? (_colors[source.substring('FColors.'.length)] ?? source)
                        : source;
                  }
                }

                final constructor = (theme: theme, variant: variant, colors: colors);
                (themes[(theme, null)] ??= []).add(constructor);
                (themes[(theme, constructor.variant)] ??= []).add(constructor);
              }
            }
          }
        }
      }
    }
  }
}
