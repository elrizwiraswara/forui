typedef ThemesConstructors = ({
  Map<String, ConstructorMatch> typography,
  Map<String, ConstructorMatch> style,
  Map<String, String> icons,
  Map<(String, String?), List<ThemeConstructor>> themes,
});

typedef ThemeConstructor = ({String theme, String variant, String colors});

typedef ThemeFragment = ({String theme, String colors, String typography, String style});

final _colors = p.join(library, 'src', 'theme', 'colors.dart');
final _icons = p.join(library, 'src', 'theme', 'icons.dart');
final _themes = p.join(library, 'src', 'theme', 'themes.dart');

final _typography = RegExp('FTypography');
final _typographyConstructor = RegExp(r'(FTypography)\.inherit');
final _style = RegExp(r'^FStyle$');
final _styleConstructor = RegExp(r'(FStyle)\.inherit');

String generateThemes(Map<(String, String?), ThemeFragment> fragments) {
  final registry = LibraryBuilder()
    ..comments.addAll([header])
    ..body.addAll([
      (EnumBuilder()
            ..docs.addAll(['/// All themes in Forui. Generated by tool/cli_generator.'])
            ..name = 'Theme'
            ..values.addAll([
              for (final MapEntry(key: (theme, variant), value: fragment) in fragments.entries)
                (EnumValueBuilder()
                      ..name = '$theme${variant == null ? '' : variant.capitalize()}'
                      ..arguments.addAll([
                        literalString('$theme${variant == null ? '' : '-$variant'}'),
                        literalString(fragment.theme.replaceAll(r'$', r'\$')),
                        literalString(fragment.colors.replaceAll(r'$', r'\$')),
                        literalString(fragment.typography.replaceAll(r'$', r'\$')),
                        literalString(fragment.style.replaceAll(r'$', r'\$')),
                      ]))
                    .build(),
            ])
            ..fields.addAll([
              (FieldBuilder()
                    ..docs.addAll(['/// The name.'])
                    ..name = 'name'
                    ..type = refer('String')
                    ..modifier = FieldModifier.final$)
                  .build(),
              (FieldBuilder()
                    ..docs.addAll(['/// The theme.dart source.'])
                    ..name = 'source'
                    ..type = refer('String')
                    ..modifier = FieldModifier.final$)
                  .build(),
              (FieldBuilder()
                    ..docs.addAll(['/// The colors.dart source.'])
                    ..name = 'colors'
                    ..type = refer('String')
                    ..modifier = FieldModifier.final$)
                  .build(),
              (FieldBuilder()
                    ..docs.addAll(['/// The typography.dart source.'])
                    ..name = 'typography'
                    ..type = refer('String')
                    ..modifier = FieldModifier.final$)
                  .build(),
              (FieldBuilder()
                    ..docs.addAll(['/// The style.dart source.'])
                    ..name = 'style'
                    ..type = refer('String')
                    ..modifier = FieldModifier.final$)
                  .build(),
            ])
            ..constructors.add(
              (ConstructorBuilder()
                    ..constant = true
                    ..requiredParameters.addAll([
                      Parameter((p) => p..name = 'this.name'),
                      Parameter((p) => p..name = 'this.source'),
                      Parameter((p) => p..name = 'this.colors'),
                      Parameter((p) => p..name = 'this.typography'),
                      Parameter((p) => p..name = 'this.style'),
                    ]))
                  .build(),
            ))
          .build(),
    ]);

  return metaFormatter.format(registry.build().accept(emitter).toString());
}

Map<(String, String?), ThemeFragment> mapThemes(ThemesConstructors themes) {
  final typography = ConstructorFragment.inline(_typographyConstructor, themes.typography).values.single;
  final icons = StringBuffer('FIcons(\n');
  for (final MapEntry(:key, :value) in themes.icons.entries) {
    icons.writeln('  $key: $value,');
  }
  icons.writeln(');');
  final style = ConstructorFragment.inline(_styleConstructor, themes.style).values.single;

  final fragments = <(String, String?), ThemeFragment>{};
  for (final MapEntry(:key, value: constructors) in themes.themes.entries) {
    final themeBuffer = StringBuffer();
    final colorsBuffer = StringBuffer();
    final typographyBuffer = StringBuffer();
    final styleBuffer = StringBuffer();

    for (final constructor in constructors) {
      final name = '${constructor.theme}${constructor.variant.capitalize()}';
      final colors = '${constructor.variant}Colors';

      themeBuffer.writeln('''
        FThemeData get $name {
          // Change this to false to use the desktop variant of this theme.
          const touch = true;
          
          final colors = $colors;
          
          final typography = _typography(colors: colors, touch: touch);
          
          final icons = $icons
          
          final style = _style(colors: colors, typography: typography, touch: touch);

          return FThemeData(
            colors: colors,
            typography: typography,
            icons: icons,
            style: style,
            touch: touch,
          );
        }

        ''');

      colorsBuffer
        ..writeln(
          'final FColors $colors = ${constructor.colors.replaceFirst(RegExp(r',?\s*\)\s*$'), ', extensions: const [AppColors()])')};',
        )
        ..writeln();
